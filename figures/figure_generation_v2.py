    def _load_data_type(self, data_type: str, filename_pattern: str = None) -> List[Dict]:
        """Load most recent data of specified type."""
        if filename_pattern is None:
            filename_pattern = data_type
            
        try:
            # Look for JSON files with the data type
            pattern = os.path.join(self.data_dir, data_type, f"{filename_pattern}_*.json")
            files = glob(pattern)
            
            if not files:
                print(f"Warning: No {data_type} data files found at {pattern}")
                return []
            
            # Use most recent file
            latest_file = sorted(files)[-1]
            print(f"Loading {data_type} data from: {os.path.basename(latest_file)}")
            
            with open(latest_file, 'r') as f:
                data = json.load(f)
            
            return data if isinstance(data, list) else []
            
        except Exception as e:
            print(f"Error loading {data_type} data: {e}")
            return []
    
    def plot_grafe_figure4_analog(self, save_format: str = 'pdf') -> Optional[str]:
        """
        Create analog of Grafe's Figure 4: Logical error vs physical error rate 
        for different code sizes, showing threshold behavior.
        Updated to use streaming protocol data.
        """
        # Try streaming threshold data first, fall back to legacy
        threshold_data = self.threshold_data if self.threshold_data else []
        
        if not threshold_data:
            print("Warning: No threshold data available for Grafe Figure 4 analog")
            return None
        
        fig, ax = plt.subplots(figsize=(10, 8))
        
        # Auto-discover available noise types and focus on depolarizing
        available_noise_types = list(set(d['noise_type'] for d in threshold_data))
        print(f"Available noise types: {available_noise_types}")
        
        # Focus on depolarizing noise for main result
        depolarizing_data = [d for d in threshold_data if d['noise_type'] == 'depolarizing']
        
        if not depolarizing_data:
            print("Warning: No depolarizing data for Figure 4 analog")
            return None
        
        # Auto-discover all available code sizes and sort them
        code_sizes = sorted(list(set(d['N'] for d in depolarizing_data)))
        print(f"Found code sizes: {code_sizes}")
        
        # Generate colors dynamically based on number of code sizes
        colors = plt.cm.viridis(np.linspace(0, 1, len(code_sizes)))
        
        plotted_curves = 0
        
        # Plot threshold curves for each code size
        for i, N in enumerate(code_sizes):
            # Find data for this code size
            size_data = [d for d in depolarizing_data if d['N'] == N]
            
            if size_data:
                data = size_data[0]  # Should only be one per code size
                
                physical_rates = np.array(data['physical_error_rates'])
                
                # Use best_final_errors if available (streaming), otherwise final_logical_errors (legacy)
                if 'best_final_errors' in data:
                    final_errors = np.array(data['best_final_errors'])
                else:
                    final_errors = np.array(data['final_logical_errors'])
                
                print(f"Code size N={N}: {len(physical_rates)} error rate points")
                
                # Filter out invalid data
                valid_mask = np.isfinite(final_errors) & (final_errors > 0)
                
                if np.sum(valid_mask) > 0:
                    valid_physical = physical_rates[valid_mask]
                    valid_final = final_errors[valid_mask]
                    
                    ax.loglog(valid_physical, valid_final, 'o-', 
                             color=colors[i], linewidth=3, markersize=8,
                             label=f'N = {N}', alpha=0.8)
                    plotted_curves += 1
        
        if plotted_curves == 0:
            print("Warning: No valid curves to plot")
            ax.text(0.5, 0.5, 'No Valid Threshold Data', 
                   transform=ax.transAxes, ha='center', va='center', fontsize=16)
            return None
        
        # Add "no improvement" reference line
        physical_range = np.logspace(-2, 0, 100)
        ax.loglog(physical_range, physical_range, '--', 
                 color='gray', linewidth=2, alpha=0.7, label='No Correction')
        
        ax.set_xscale('linear')
        ax.set_xlabel(r'Physical Error Rate, $\delta$', fontsize=25)
        ax.set_ylabel(r'Final Logical Error Rate, $\varepsilon_L$', fontsize=25)
        ax.set_title('Streaming QEC Threshold \n(Depolarizing Noise, d=2)', fontsize=30)
        
        ax.legend(fontsize=14, loc='upper left')
        ax.set_xlim(0.09, 1.0)
        ax.set_ylim(1e-5, 1.0)
        
        plt.tight_layout()
        
        filename = f"grafe_figure4_analog.{save_format}"
        filepath = os.path.join(self.figures_dir, filename)
        plt.savefig(filepath, dpi=300, bbox_inches='tight', format=save_format)
        plt.close()
        
        print(f"Saved Grafe Figure 4 analog: {filename} with {plotted_curves} curves")
        return filepath
    
    def plot_dimension_sweep_figure1a(self, save_format: str = 'pdf') -> Optional[str]:
        """
        Figure 1a (dimension sweep): Final logical error vs physical error rate
        for multiple dimensions d, at a fixed code size N_target.
        Uses streaming threshold data if present.
        """
        N_target = 1024 
        noise_type: str = 'depolarizing'
        dims_to_plot: Optional[list] = None
        threshold_data = self.threshold_data if self.threshold_data else []
        if not threshold_data:
            print("Warning: No threshold data available for dimension sweep")
            return None

        # Filter by noise type
        data_nt = [d for d in threshold_data if d.get('noise_type') == noise_type]
        if not data_nt:
            print(f"Warning: No data for noise_type='{noise_type}'")
            return None

        # What dimensions do we have?
        all_dims = sorted({d.get('dimension') for d in data_nt})
        if dims_to_plot is None:
            dims = all_dims
        else:
            dims = [d for d in dims_to_plot if d in all_dims]
            missing = set(dims_to_plot) - set(dims)
            if missing:
                print(f"Note: requested dims missing from data: {sorted(missing)}")

        # Helper: pick the record at (dimension=d, N=N_target). If absent, take closest N.
        def pick_record_for_dim(d):
            candidates = [r for r in data_nt if r.get('dimension') == d]
            exact = [r for r in candidates if r.get('N') == N_target]
            if exact:
                return exact[0]
            # Fallback to nearest N (use log-distance to be kind to powers of two)
            if not candidates:
                return None
            return min(candidates, key=lambda r: abs(np.log2(r.get('N')) - np.log2(N_target)))

        # Build plot
        fig, ax = plt.subplots(figsize=(10, 8))
        colors = plt.cm.plasma(np.linspace(0, 1, len(dims)))
        plotted = 0

        for i, d in enumerate(dims):
            rec = pick_record_for_dim(d)
            if rec is None:
                print(f"Skipping d={d}: no records found")
                continue

            phys = np.array(rec.get('physical_error_rates', []), dtype=float)

            # Prefer streaming output if present
            if 'best_final_errors' in rec and rec['best_final_errors'] is not None:
                final = np.array(rec['best_final_errors'], dtype=float)
            else:
                final = np.array(rec.get('final_logical_errors', []), dtype=float)

            # Filter valid points
            valid = np.isfinite(phys) & np.isfinite(final) & (final > 0)
            if not np.any(valid):
                print(f"Skipping d={d}: no valid points")
                continue

            label_N = rec.get('N', N_target)
            ax.loglog(phys[valid], final[valid], 'o-', 
                    linewidth=3, markersize=8, alpha=0.85,
                    color=colors[i], label=f'd = {d} (N={label_N})')
            plotted += 1

        if plotted == 0:
            print("Warning: No valid curves to plot for dimension sweep")
            ax.text(0.5, 0.5, 'No Valid Dimension Data',
                    transform=ax.transAxes, ha='center', va='center', fontsize=16)
            return None

        # "No correction" reference
        physical_range = np.logspace(-2, 0, 100)
        ax.loglog(physical_range, physical_range, '--',
                color='gray', linewidth=2, alpha=0.7, label='No Correction')

        # Match your existing styling choices
        ax.set_xscale('linear')  # keep consistent with your current fig 1a
        ax.set_xlabel(r'Physical Error Rate, $\delta$', fontsize=25)
        ax.set_ylabel(r'Final Logical Error Rate, $\varepsilon_L$', fontsize=25)
        ax.set_title(f'Streaming Threshold \nAcross Dimensions (Depolarizing, N={N_target})', fontsize=30, pad=16)

        ax.legend(fontsize=14, loc='upper left', ncol=1)
        ax.set_xlim(0.09, 1.0)
        ax.set_ylim(1e-5, 1.0)
        plt.tight_layout()

        filename = f"figure_dimension_sweep_N{N_target}.{save_format}"
        filepath = os.path.join(self.figures_dir, filename)
        # plt.tight_layout()
        plt.savefig(filepath, dpi=300, bbox_inches='tight', format=save_format)
        plt.close()

        print(f"Saved dimension sweep figure: {filename} with {plotted} curves")
        return filepath

    

    def plot_error_reduction_ratios(self, save_format: str = 'pdf') -> Optional[str]:
        """Plot error reduction ratios showing improvement factors."""
        if not self.threshold_data:
            print("Warning: No threshold data for error reduction ratios")
            return None
        
        fig, ax = plt.subplots(figsize=(10, 8))
        
        # Focus on depolarizing noise
        depolarizing_data = [d for d in self.threshold_data if d['noise_type'] == 'depolarizing']
        
        # Get different code sizes
        code_sizes = sorted(list(set(d['N'] for d in depolarizing_data)))
        colors = plt.cm.viridis(np.linspace(0, 1, len(code_sizes)))
        
        plotted_any = False
        
        for i, N in enumerate(code_sizes):
            matches = [d for d in depolarizing_data if d['N'] == N]
            
            if matches:
                data = matches[0]
                physical_rates = np.array(data['physical_error_rates'])
                reduction_ratios = np.array(data['error_reduction_ratios'])
                
                # Filter valid data
                valid_mask = np.isfinite(reduction_ratios) & (reduction_ratios > 0)
                
                if np.sum(valid_mask) > 0:
                    valid_physical = physical_rates[valid_mask]
                    valid_ratios = reduction_ratios[valid_mask]
                    
                    ax.semilogy(valid_physical, valid_ratios, 'o-', 
                               color=colors[i], linewidth=3, markersize=8,
                               label=f'N = {N}')
                    plotted_any = True
        
        if plotted_any:
            # Add reference line for no improvement
            ax.axhline(y=1.0, color='gray', linestyle='--', alpha=0.7, 
                      linewidth=2, label='No Improvement')

        ax.set_xlabel(r'Physical Error Rate, $\delta$', fontsize=25)
        ax.set_ylabel('Error Reduction Ratio', fontsize=25)
        ax.set_title('Error Reduction Effectiveness', fontsize=30)
        ax.legend(fontsize=14)
        # ax.grid(True, alpha=0.3)
        
        plt.tight_layout()
        
        filename = f"error_reduction_ratios.{save_format}"
        filepath = os.path.join(self.figures_dir, filename)
        plt.savefig(filepath, dpi=300, bbox_inches='tight', format=save_format)
        plt.close()
        
        print(f"Saved error reduction ratios plot: {filename}")
        return filepath
    

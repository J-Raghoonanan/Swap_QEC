    def plot_resource_overhead(self, save_format: str = 'pdf') -> Optional[str]:
        """Plot resource overhead vs code size."""
        if not self.resource_data:
            print("Warning: No resource data available")
            return None
        
        fig, ax = plt.subplots(figsize=(10, 8))
        
        # Extract data for depolarizing noise
        depolarizing_resources = [d for d in self.resource_data if d['noise_type'] == 'depolarizing']
        
        if depolarizing_resources:
            N_values = sorted(list(set(d['N'] for d in depolarizing_resources)))
            total_operations = []
            amp_iterations = []
            
            for N in N_values:
                matches = [d for d in depolarizing_resources if d['N'] == N]
                if matches:
                    data = matches[0]
                    total_operations.append(data['total_swap_operations'])
                    amp_iterations.append(data['total_amplification_iterations'])
            
            if len(N_values) > 0:
                ax.loglog(N_values, total_operations, 'o-', color='blue',
                         label='Swap Operations', linewidth=3, markersize=8)
                ax.loglog(N_values, amp_iterations, 's-', color='red',
                         label='Amplification Iterations', linewidth=3, markersize=8)
                
                # Add scaling reference
                ref_linear = [N * 0.5 for N in N_values]
                ax.loglog(N_values, ref_linear, '--', color='gray', alpha=0.7, 
                         label='Linear Scaling')

        ax.set_xlabel('Code Size (N)', fontsize=16)
        ax.set_ylabel('Total Operations', fontsize=16)
        ax.set_title('Resource Overhead Scaling', fontsize=18)
